export function referenceFileName(item) {
	const slug = item.name.normalize('NFD').replace(/[\u0300-\u036f]/g, '').toLowerCase()
		.replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
	return `${String(item.id).padStart(2, '0')}-${slug}.png`;
}
